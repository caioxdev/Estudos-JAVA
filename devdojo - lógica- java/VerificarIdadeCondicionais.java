/* Exercício - Condicionais
Receba uma idade com entrada
Se a idade for maior que 18 imprima "Adulto"
Senão imprima "Não Adulto"
 */

public class VerificarIdadeCondicionais {
    public static void main(String[] args) {
        int idade = 18;

        if (idade >= 18) {
            System.out.println("Adulto");
        } else {
            System.out.println("Não Adulto");
        }
    }
}
