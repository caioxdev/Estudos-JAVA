public class CondicionaisAula1 {
    public static void main(String[] args) {
        boolean condicao = true;
        if (condicao) {
            System.out.println("Dentro do IF");
        } else {
            System.out.println("Dentro do ELSE");
        }
        System.out.println("Fora do IF");
    }
}

/*
Condionais são blocos de códigos que vão ser executados se o valor dentro do colchetes for verdadeiro.
Sintaxe IF: if(conteúdo){}
Sintaxe ELSE: else(conteúdo){}

 */